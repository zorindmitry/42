#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int 	i;
	char *str;

	str = (char *)s;
	i = 0;
	while (str[i] != (char)c)
	{
		if (str[i] == '\0')
			return (NULL);
		i++;
	}
	return (&str[i]);
}
